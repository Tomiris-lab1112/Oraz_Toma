#которая находит строку, содержащую символ 'a', за которым следует что угодно, и заканчивается символом 'b'.

import re

pattern = r'a.*b'  # 'a' + любые символы + 'b' в конце

test_strings = ["ab", "acb", "a123b", "aXYZb", "abbb", "ba", "abc"]

for s in test_strings:
    if re.fullmatch(pattern, s):
        print(f"Совпадение найдено: {s}")
    else:
        print(f"Нет совпадения: {s}")

#a — строка должна содержать символ 'a' в начале.
#.* — за 'a' может идти любое количество любых символов (в том числе и ни одного).
#b — строка должна заканчиваться на 'b'.
#re.fullmatch() проверяет, что вся строка соответствует шаблону.
