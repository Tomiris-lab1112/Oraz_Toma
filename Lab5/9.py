#которая вставляет пробелы между словами, начинающимися с заглавной буквы.

import re

# Исходная строка
text = "ThisIsAnExampleOfCamelCase"

# Вставляем пробелы перед словами, начинающимися с заглавной буквы (кроме первой)
result = re.sub(r'(?<!^)(?=[A-Z])', ' ', text)

print("Исходная строка:", text)
print("Результат:", result)

#Краткое объяснение:

#re.sub() — функция замены по шаблону.
#(?<!^) — проверка, что мы не в начале строки.
#(?=[A-Z]) — ищет позицию перед заглавной буквой.
#Вместе шаблон (?<!^)(?=[A-Z]) вставляет пробел перед каждой заглавной буквой, кроме самой первой.