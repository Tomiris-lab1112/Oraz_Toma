#которая находит последовательности строчных букв, соединённых символом подчёркивания (_).
import re

pattern = r'[a-z]+_[a-z]+'  # строчные буквы + '_' + строчные буквы

test_strings = ["hello_world", "test_case", "Hello_World", "no_underscore", "snake_case_test"]

for s in test_strings:
    if re.fullmatch(pattern, s):
        print(f"Совпадение найдено: {s}")
    else:
        print(f"Нет совпадения: {s}")

#import re — подключаем модуль регулярных выражений.
#r'[a-z]+_[a-z]+' — шаблон:
#[a-z]+ — одна или больше строчных букв,
#_ — символ подчёркивания,
#[a-z]+ — снова одна или больше строчных букв.
#re.fullmatch() — проверяет, совпадает ли вся строка с шаблоном.