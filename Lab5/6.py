#которая заменяет все пробелы, запятые и точки на двоеточие (:)

import re

text = "Hello, world. This is a test."

# Заменяем пробелы, запятые и точки на двоеточие
result = re.sub(r'[ ,.]', ':', text)

print(result)


#r'[ ,.]' — регулярное выражение, которое ищет пробелы, запятые и точки.
#re.sub() — функция, которая заменяет все найденные символы на двоеточие :.
#В результате все пробелы, запятые и точки в строке заменятся на :.