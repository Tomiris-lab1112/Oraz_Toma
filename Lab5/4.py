#которая находит последовательности, состоящие из одной заглавной буквы, за которой следуют строчные буквы.
import re

pattern = r'[A-Z][a-z]+'  # Одна заглавная буква + одна или более строчных букв

test_strings = ["Hello", "World", "hello", "WORLD", "Python", "PyThon"]

for s in test_strings:
    if re.fullmatch(pattern, s):
        print(f"Совпадение найдено: {s}")
    else:
        print(f"Нет совпадения: {s}")

#[A-Z] — одна заглавная буква от A до Z.
#[a-z]+ — одна или более строчных букв.
#re.fullmatch() — проверяет, совпадает ли вся строка с этим шаблоном.

#Что программа делает:
#Перебирает список строк.
#Проверяет каждую строку по шаблону.
#Выводит, подходит ли строка под условие: одна заглавная буква, за которой идут строчные.