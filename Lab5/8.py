#которая разделяет строку по заглавным буквам.

import re

text = "SplitThisStringByUppercaseLetters"

# Разделяем строку перед каждой заглавной буквой
result = re.split(r'(?=[A-Z])', text)

print(result)


#Краткое объяснение:

#re.split() — делит строку по шаблону.
#r'(?=[A-Z])' — это положительный просмотр вперёд (lookahead),
#он не удаляет буквы, а просто говорит: «раздели перед каждой заглавной».
#В итоге строка разбивается на части, начиная с каждой большой буквы.