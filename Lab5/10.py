#которая преобразует строку из camelCase в snake_case.

import re

# Исходная строка
text = "thisIsCamelCaseExample"

# Преобразуем из camelCase в snake_case
result = re.sub(r'(?<!^)(?=[A-Z])', '_', text).lower()

print("Исходная строка:", text)
print("Результат:", result)

#Краткое объяснение:

#re.sub() — заменяет все совпадения шаблона на _.
#(?<!^) — не ставим подчёркивание в начале строки.
#(?=[A-Z]) — находим позицию перед заглавной буквой.
#.lower() — переводим всё в строчные буквы, чтобы получилось в стиле snake_case.