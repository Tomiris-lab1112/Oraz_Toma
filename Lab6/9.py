#8. Которая удаляет файл по указанному пути, предварительно проверяя, существует ли файл и есть ли права на его удаление

import os

def удалить_файл(путь):
    if not os.path.exists(путь):
        print(f"Файл по пути '{путь}' не существует.")
        return
    
    if not os.access(путь, os.W_OK):
        print(f"Нет прав на удаление файла '{путь}'.")
        return
    
    try:
        os.remove(путь)
        print(f"Файл '{путь}' успешно удалён.")
    except Exception as e:
        print(f"Ошибка при удалении файла: {e}")

# Запрос пути к файлу
путь_файла = input("Введите путь к файлу для удаления: ")
удалить_файл(путь_файла)
