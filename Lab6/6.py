#5. Которая записывает элементы списка в файл — каждый элемент с новой строки.

def записать_список_в_файл(список, имя_файла):
    with open(имя_файла, 'w', encoding='utf-8') as файл:
        for элемент in список:
            файл.write(str(элемент) + '\n')

# Пример использования
мой_список = ['яблоко', 'банан', 'вишня', 42, True]
имя_файла = 'вывод.txt'

записать_список_в_файл(мой_список, имя_файла)
print(f"Список записан в файл {имя_файла}")


'что будет в файле вывод.txt:'
'яблоко'
'банан'
'вишня'
'42'
'True'