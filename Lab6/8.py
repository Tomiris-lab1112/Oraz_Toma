#7. Которая копирует содержимое одного файла в другой

def копировать_файл(источник, назначение):
    try:
        with open(источник, 'r', encoding='utf-8') as файл_источника:
            содержимое = файл_источника.read()
        with open(назначение, 'w', encoding='utf-8') as файл_назначения:
            файл_назначения.write(содержимое)
        print(f"Содержимое скопировано из {источник} в {назначение}")
    except FileNotFoundError:
        print("Файл-источник не найден.")
    except Exception as e:
        print(f"Произошла ошибка: {e}")

# Запрос путей у пользователя
путь_источника = input("Введите путь к исходному файлу: ")
путь_назначения = input("Введите путь к файлу назначения: ")
копировать_файл(путь_источника, путь_назначения)
