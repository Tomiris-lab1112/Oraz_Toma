#6. Которая создаёт 26 текстовых файлов с именами от A.txt до Z.txt:

import string

def создать_файлы():
    for буква in string.ascii_uppercase:  # буквы от 'A' до 'Z'
        имя_файла = f"{буква}.txt"
        with open(имя_файла, 'w', encoding='utf-8') as файл:
            файл.write(f"Это файл {имя_файла}\n")
    print("Создано 26 файлов: от A.txt до Z.txt")

создать_файлы()

'После запуска программы в текущей папке появятся файлы:'
'A.txt, B.txt, C.txt, ..., Z.txt'
'с содержимым, например, в файле A.txt:'
'Это файл A.txt'