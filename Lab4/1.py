#Python iterators and generators
#1. Генератор квадратов чисел до N
'''
Это функция-генератор, которая возвращает квадраты чисел от 0 до N (включительно).
Ключевое слово yield позволяет функции возвращать значение, 
но не завершать выполнение — можно продолжить с того же места.
'''
def square_generator(N):
    for i in range(N + 1):
        yield i ** 2

"Пример"
for num in square_generator(5):
    print(num)
"Выведет"
0, 1, 4, 9, 16, 25


#2. Программа, которая печатает чётные числа от 0 до n через запятую, используя генератор
'''
Объяснение:
Функция even_number_generator генерирует чётные числа от 0 до n.
Вводим n с клавиатуры.
map(str, even_numbers) превращает числа в строки.
','.join(...) объединяет их в одну строку с запятыми.
'''
def even_number_generator(n):
    for i in range(n + 1):
        if i % 2 == 0:
            yield i
n = int(input("Введите число: "))
even_numbers = even_number_generator(n)
print(','.join(map(str, even_numbers)))
"Если ввели 8, программа выведет:"
0,2,4,6,8


#3. Генератор чисел, делящихся на 3 и 4 в диапазоне от 0 до n
'''
Объяснение:
Функция ищет числа, которые делятся и на 3, 
и на 4 (то есть на 12) в диапазоне от 0 до n, и по одному выдаёт их через yield.
'''
def div_by_3_and_4(n):
    for i in range(n + 1):
        if i % 3 == 0 and i % 4 == 0:
            yield i
"Пример использования:"
for num in div_by_3_and_4(50):
    print(num)
"Результат: 0, 12, 24, 36, 48"


#4. Генератор squares, который выдаёт квадраты чисел от a до b
'''
Объяснение:
Выдаёт квадраты чисел от a до b, включая оба конца диапазона.
'''
def squares(a, b):
    for i in range(a, b + 1):
        yield i ** 2
# Пример использования
for square in squares(3, 7):
    print(square)
"Если a = 3, b = 7, выведет: 9, 16, 25, 36, 49"


#5. Генератор, возвращающий числа от n до 0 (обратный отсчёт)
"""
Объяснение:
Генератор делает обратный отсчёт от n до 0.
range(n, -1, -1) означает: начинаем с n, идём до -1 (не включая), с шагом -1.
"""
def countdown(n):
    for i in range(n, -1, -1):
        yield i
# Пример использования
for num in countdown(5):
    print(num)

"Результат при n = 5: "
"5, 4, 3, 2, 1, 0"